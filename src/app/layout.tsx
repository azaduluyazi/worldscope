export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const locale = await getLocale();
  const messages = await getMessages();

  return (
    <html lang={locale} className={`dark ${jetbrainsMono.variable} ${inter.variable} ${orbitron.variable} ${rajdhani.variable} ${shareTechMono.variable} ${syne.variable} ${cormorant.variable}`}>
      <head>
        {/* PWA & Mobile meta */}
        <meta name="theme-color" content="#050a12" />
        <link rel="apple-touch-icon" href="/icons/icon-192.svg" />
        {/* RSS autodiscovery */}
        <link rel="alternate" type="application/rss+xml" title="TroiaMedia RSS Feed" href="/feed.xml" />
        {/* Performance: DNS prefetch + preconnect for external APIs */}
        <link rel="dns-prefetch" href="https://api.mapbox.com" />
        <link rel="dns-prefetch" href="https://img.youtube.com" />
        <link rel="dns-prefetch" href="https://opensky-network.org" />
        <link rel="preconnect" href="https://api.mapbox.com" crossOrigin="anonymous" />
        <link rel="preconnect" href="https://img.youtube.com" crossOrigin="anonymous" />
        <script
          type="application/ld+json"
          dangerouslySetInnerHTML={{ __html: JSON.stringify(jsonLd) }}
        />
        {ADSENSE_PUB_ID && (
          <>
            {/* Google AdSense ownership verification — recommended method for
                site approval. Must match the publisher ID used in the script
                below and in /ads.txt. */}
            <meta name="google-adsense-account" content={ADSENSE_PUB_ID} />
            <Script
              src={`https://pagead2.googlesyndication.com/pagead/js/adsbygoogle.js?client=${ADSENSE_PUB_ID}`}
              strategy="afterInteractive"
              crossOrigin="anonymous"
            />
          </>
        )}
        {/* Plausible Analytics — privacy-friendly, cookie-free.
            Uses the newer Plausible Analytics (PA) script format where the
            site id is embedded in the script path. Set
            NEXT_PUBLIC_PLAUSIBLE_SCRIPT_ID in Vercel env to the literal id
            shown in the Plausible dashboard (e.g. "pa-TsvQfIzzKqn4Ko-W_Z6bE").
            Falls back to the legacy data-domain script if the new id is
            not configured but NEXT_PUBLIC_PLAUSIBLE_DOMAIN is. */}
        {process.env.NEXT_PUBLIC_PLAUSIBLE_SCRIPT_ID && (
          <>
            <Script
              async
              src={`https://plausible.io/js/${process.env.NEXT_PUBLIC_PLAUSIBLE_SCRIPT_ID}.js`}
              strategy="afterInteractive"
            />
            <Script id="plausible-init" strategy="afterInteractive">
              {`window.plausible=window.plausible||function(){(plausible.q=plausible.q||[]).push(arguments)},plausible.init=plausible.init||function(i){plausible.o=i||{}};plausible.init()`}
            </Script>
          </>
        )}
        {!process.env.NEXT_PUBLIC_PLAUSIBLE_SCRIPT_ID &&
          process.env.NEXT_PUBLIC_PLAUSIBLE_DOMAIN && (
            <Script
              defer
              data-domain={process.env.NEXT_PUBLIC_PLAUSIBLE_DOMAIN}
              src={process.env.NEXT_PUBLIC_PLAUSIBLE_HOST
                ? `${process.env.NEXT_PUBLIC_PLAUSIBLE_HOST}/js/script.js`
                : "https://plausible.io/js/script.js"}
              strategy="lazyOnload"
            />
          )}
      </head>
      <body className="min-h-screen bg-hud-base text-hud-text overflow-hidden">
        {/* Skip to content — accessibility */}
        <a
          href="#main-content"
          className="sr-only focus:not-sr-only focus:fixed focus:top-2 focus:left-2 focus:z-[9999] focus:bg-hud-accent focus:text-hud-base focus:px-4 focus:py-2 focus:rounded focus:font-mono focus:text-xs"
        >
          Skip to content
        </a>
        <ClerkProvider
          appearance={{
            variables: {
              colorPrimary: "#f5a524",
              colorBackground: "#060509",
              colorText: "#f1ede3",
              colorInputBackground: "#110d14",
              colorInputText: "#f1ede3",
              borderRadius: "2px",
              // Keep Clerk's own font stack so sizing/readability aren't
              // collapsed by the dashboard's mono font.
              fontSize: "15px",
            },
            elements: {
              rootBox: "text-[15px]",
              card: "bg-[#0a0810] border border-[#2d1e08] shadow-[0_0_24px_-12px_rgba(245,165,36,.4)] text-[15px]",
              cardBox: "text-[15px]",
              headerTitle: "text-[#f1ede3] text-xl tracking-wide",
              headerSubtitle: "text-gray-400 text-sm",
              socialButtonsBlockButton: "border-[#2d1e08] hover:bg-[#f5a524]/10 text-[14px]",
              formButtonPrimary: "bg-[#f5a524] text-[#060509] hover:bg-[#ffc55a] text-[14px] font-semibold",
              formFieldLabel: "text-[#c5bfae] text-[13px]",
              formFieldInput: "text-[15px]",
              footerActionLink: "text-[#f5a524] hover:text-[#ffc55a]",
              dividerLine: "bg-[#2d1e08]",
              dividerText: "text-gray-500 text-[12px]",
            },
          }}
          signInFallbackRedirectUrl="/"
          signUpFallbackRedirectUrl="/"
        >
          <NextIntlClientProvider locale={locale} messages={messages}>
            <ThemeProvider>
              <WebVitals />
              <main id="main-content">
                {children}
              </main>
              <ScrollToTop />
              <NewsletterStickyBar />
              <AdConsentBanner />
              <div className="scanlines" aria-hidden="true" />
            </ThemeProvider>
          </NextIntlClientProvider>
        </ClerkProvider>
        {/* Service Worker registration — enables offline cache for the
            PWA install. Registered lazyOnload so it never blocks first
            paint. The SW itself lives at public/sw.js and is opt-in by
            manifest.json. Skipped on localhost so dev reloads aren't
            cached. */}
        <Script id="sw-register" strategy="lazyOnload">
          {`if('serviceWorker' in navigator && location.hostname !== 'localhost'){navigator.serviceWorker.register('/sw.js').catch(()=>{})}`}
        </Script>
      </body>
    </html>
  );
}
